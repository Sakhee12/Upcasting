package PrinciplesOfOops;

public class Chrome_Webdriver extends Webdriver
{
	public void ChromeProperties()
	{
		System.out.println("Properties of Chrome");
	}
	
	public void Permission()
	{
		System.out.println("Camera On/Off");
	}
	
	public void LogFileFeature()
	{
		System.out.println("Append log");
		System.out.println("Readable Timestamps");
	}
	
	public void Extension()
	{
		System.out.println("The extensions parameter accepts crx files. As for unpacked directories, please use the load-extension argument instead");
	}
}
